package sample;

public class Finger {

	public Boolean hasLongNails;
	public Hand hand;

	public Boolean getHasLongNails() {
		return hasLongNails;
	}

	public void setHasLongNails(Boolean hasLongNails) {
		this.hasLongNails = hasLongNails;
	}

	public Hand getHand() {
		return hand;
	}

	public void setHand(Hand hand) {
		this.hand = hand;
	}

}
